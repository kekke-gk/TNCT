int main()
{
	int a,x,y,z;
	scanf("%d",&a);
	z=a%10;
	y=(a%100-z)/10;
	x=(a%1000-y*10-z)/100;
	printf("100=>%d\n",x);
	printf("010=>%d\n",y);
	printf("001=>%d\n",z);

	return 0;
}


