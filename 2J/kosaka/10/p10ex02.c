#include <stdio.h>

char board[8][8]; /*クインを置いてある場所は1，そうでない場所は0で表わすものとする。*/

void printBoard(void)
{
	int i,j;
	static int count=1;
	printf("#%d is found.\n",count);
	for(i=0;i<8;i++){
		for(j=0;j<8;j++){
			printf("%2c",board[i][j]);
		}
		printf("\n");
	}
	printf("\n");
	count++;
}

/*column列に関する作業*/
void columnwork(int column)
{
	int row;
	int status;
	int i,n;
	if (column==8) { /*column=8でこの関数が呼び出されたら，解が見つかっているということなので*/
		printBoard(); /*発見された解を表示*/
		return;
	}
	for (row=0; row<8; row++) {/*row=0〜7まで検査*/
		status=1;

		//左にクイーンがあるかチェック
		for(i=0;i<column;i++){
			if(board[column-i-1][row]=='1'){
				status=0;
				break;
			}
		}
		
		//左上にクイーンが置いてあるかチェック
		if(status==1){
			if(column<row) n=column;
			else n=row;
			for(i=0;i<n;i++){
				if(board[column-i-1][row-i-1]=='1'){
					status=0;
					break;
				}
			}
		}
		
		//左下にクイーンが置いてあるかチェック
		if(status==1){
			if(column<7-row) n=column;
			else n=7-row;
			for(i=0;i<n;i++){
				if(board[column-i-1][row+i+1]=='1'){
					status=0;
					break;
				}
			}
		}

		if (status==1) { /*row行column列の位置にクインが置けるなら*/

			/* row行column列の位置にクインを置く */
			/* （board[8][8]のその位置を1にする）*/
			board[column][row]='1';

			columnwork(column+1); /*（column+1）列の作業をする*/

			/* row行column列の位置のクインをはずす */
			/* （board[8][8]のその位置を0にする）*/
			board[column][row]='0';
		}
	}
}

void initBoard(void)
{
	int i,j;
	for(i=0;i<8;i++){
		for(j=0;j<8;j++){
			board[i][j]='0';
		}
	}
}

int main()
{
	initBoard();
	columnwork(0);/*最初は第0列から作業を開始*/
	return 0;
}

